export function getCategoriesBySection(section: string): ContentCategory[] {
  return AAMC_TAXONOMY.filter(
    (c) => c.section === section && c.parentId === null,
  );
}

export function getChildCategories(parentId: string): ContentCategory[] {
  return AAMC_TAXONOMY.filter((c) => c.parentId === parentId);
}

export function getCategoryById(id: string): ContentCategory | undefined {
  return AAMC_TAXONOMY.find((c) => c.id === id);
}

export function getLeafCategories(section?: string): ContentCategory[] {
  const parentIds = new Set(
    AAMC_TAXONOMY.map((c) => c.parentId).filter(Boolean),
  );
  let result = AAMC_TAXONOMY.filter((c) => !parentIds.has(c.id));
  if (section) {
    result = result.filter((c) => c.section === section);
  }
  return result;
}
